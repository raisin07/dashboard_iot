import React, { useState, useEffect } from "react";
import { Line } from "react-chartjs-2";
import { MapContainer, TileLayer, Marker, Popup } from "react-leaflet";
import L from "leaflet";
import "leaflet/dist/leaflet.css";
import "./App.css";
import ReactPaginate from "react-paginate";
import mqtt from "mqtt"; // üöÄ Import de MQTT
import "chartjs-adapter-date-fns";

import {
  Chart as ChartJS,
  CategoryScale,
  LinearScale,
  TimeScale,
  PointElement,
  LineElement,
  Title,
  Tooltip,
  Legend,
  Filler,
} from "chart.js";
import zoomPlugin from "chartjs-plugin-zoom";

ChartJS.register(
  CategoryScale,
  LinearScale,
  TimeScale,
  PointElement,
  LineElement,
  Title,
  Tooltip,
  Legend,
  Filler,
  zoomPlugin
);

// üìç **Coordonn√©es pr√©cises de la mine**
const CAMPUS_CYBER = [48.896742, 2.233377];

// üî¥ **Ic√¥ne de la mine active**
const mineIconActive = new L.Icon({
  iconUrl: "https://upload.wikimedia.org/wikipedia/commons/e/ec/RedDot.svg",
  iconSize: [20, 20],
  iconAnchor: [10, 10],
});

// üí• **Ic√¥ne de la mine explos√©e**
const mineIconExploded = new L.Icon({
  iconUrl: "https://upload.wikimedia.org/wikipedia/commons/5/5f/BlackDot.svg",
  iconSize: [20, 20],
  iconAnchor: [10, 10],
});

// **üî¥ Seuil de d√©tection d'explosion**
const EXPLOSION_THRESHOLD = 2;
const MAX_DATA_POINTS = 500; // üìä Historique max des points affich√©s
const ITEMS_PER_PAGE = 10; // üìÑ Nombre d'√©l√©ments par page

const Dashboard = () => {
  const [data, setData] = useState([]); // üì° Historique des donn√©es re√ßues
  const [status, setStatus] = useState("Active"); // ‚úÖ √âtat de la mine
  const [currentPage, setCurrentPage] = useState(0);

  useEffect(() => {
    const options = {
      clientId: "dashboard-client-" + Math.random().toString(16).substr(2, 8),
      reconnectPeriod: 5000, // üîÑ Reconnexion automatique toutes les 5s
    };

    // ‚úÖ **Connexion √† Mosquitto WebSocket**
    const client = mqtt.connect("wss://test.mosquitto.org:8081/mqtt", options);

    client.on("connect", () => {
      console.log("‚úÖ Connect√© au broker Mosquitto !");
      client.subscribe("helium/6081F9D8CF9CEC95/rx", (err) => {
        if (!err) {
          console.log("üì° Abonn√© au topic !");
        } else {
          console.error("‚ùå Erreur d'abonnement :", err);
        }
      });
    });

    client.on("error", (err) => {
      console.error("‚ùå Erreur MQTT :", err);
    });

    client.on("close", () => {
      console.warn("‚ö†Ô∏è Connexion ferm√©e !");
    });

    client.on("message", (topic, message) => {
      console.log(`üì© Message re√ßu sur ${topic}:`, message.toString());
      try {
        const parsedMessage = JSON.parse(message.toString());

        // üî• **D√©tection d'explosion et r√©initialisation**
        const isExploded =
          Math.abs(parsedMessage.ax) > EXPLOSION_THRESHOLD ||
          Math.abs(parsedMessage.ay) > EXPLOSION_THRESHOLD ||
          Math.abs(parsedMessage.az) > EXPLOSION_THRESHOLD;

        setStatus(isExploded ? "Explos√©e" : "Active"); // ‚úÖ **Mise √† jour imm√©diate de l'√©tat**

        setData((prevData) => {
          const updatedData = [...prevData, parsedMessage]
            .sort((a, b) => new Date(a.timestamp) - new Date(b.timestamp)) // üïí Trie des donn√©es par timestamp
            .slice(-MAX_DATA_POINTS); // üîÑ Conservation de l‚Äôhistorique

          return updatedData;
        });

        // üîÑ **Met √† jour la page pour afficher les nouvelles donn√©es**
        setCurrentPage(0);

      } catch (error) {
        console.error("‚ùå Erreur de parsing du message :", error);
      }
    });

    return () => {
      client.end();
    };
  }, []);

  // üìå **Gestion de la pagination**
  const handlePageClick = ({ selected }) => {
    setCurrentPage(selected);
  };

  const offset = currentPage * ITEMS_PER_PAGE;
  const currentPageData = data.slice(offset, offset + ITEMS_PER_PAGE);
  const pageCount = Math.ceil(data.length / ITEMS_PER_PAGE);

  // üìä **Donn√©es du graphique**
  const chartData = {
    labels: data.map((d) => d.timestamp),
    datasets: [
      {
        label: "Vibration (mg)",
        data: data.map((d) => d.vibration),
        borderColor: "#2563eb",
        backgroundColor: "rgba(37, 99, 235, 0.2)",
        fill: true,
      },
    ],
  };

  const chartOptions = {
    responsive: true,
    maintainAspectRatio: false,
    animation: false,
    plugins: {
      zoom: {
        pan: { enabled: true, mode: "x" },
        zoom: { wheel: { enabled: true }, pinch: { enabled: true }, mode: "x" },
      },
    },
    scales: {
      x: {
        type: "time",
        time: {
          unit: "minute",
          displayFormats: { second: "HH:mm:ss", minute: "HH:mm", hour: "HH:mm" },
        },
        ticks: { maxRotation: 0, autoSkip: true, maxTicksLimit: 10 },
        title: { display: true, text: "Temps" },
      },
      y: { title: { display: true, text: "Vibration (mg)" } },
    },
  };

  return (
    <div className="dashboard-container">
      <h2 className="dashboard-title">Dashboard LoRaWAN - Mine</h2>
      <div className="status-container">
        <span>√âtat de la mine :</span>
        <span className={`status ${status === "Active" ? "active" : "exploded"}`}>{status}</span>
      </div>

      {/* üåç **Carte avec localisation de la mine** */}
      <MapContainer center={CAMPUS_CYBER} zoom={18} className="map-container">
        <TileLayer url="https://{s}.tile.openstreetmap.org/{z}/{x}/{y}.png" />
        <Marker position={CAMPUS_CYBER} icon={status === "Active" ? mineIconActive : mineIconExploded}>
          <Popup>{status === "Active" ? "‚ö†Ô∏è Mine active ‚ö†Ô∏è" : "üí• Mine explos√©e ! üí•"}</Popup>
        </Marker>
      </MapContainer>

      <div className="chart-container small-chart">
        <h3>Vibrations d√©tect√©es</h3>
        <Line data={chartData} options={chartOptions} />
      </div>

      <div className="table-container">
        <h3>Derniers √©v√©nements</h3>
        <table>
          <thead>
            <tr>
              <th>Heure</th>
              <th>Vibration (mg)</th>
              <th>AX</th>
              <th>AY</th>
              <th>AZ</th>
            </tr>
          </thead>
          <tbody>
            {currentPageData.map((d, index) => (
              <tr key={index}>
                <td>{new Date(d.timestamp).toLocaleTimeString()}</td>
                <td>{d.vibration.toFixed(2)}</td>
                <td>{d.ax.toFixed(2)}</td>
                <td>{d.ay.toFixed(2)}</td>
                <td>{d.az.toFixed(2)}</td>
              </tr>
            ))}
          </tbody>
        </table>
      </div>

      <ReactPaginate
  previousLabel={"‚Üê Pr√©c√©dent"}
  nextLabel={"Suivant ‚Üí"}
  breakLabel={"..."}
  pageCount={pageCount}
  marginPagesDisplayed={1}
  pageRangeDisplayed={3}
  onPageChange={handlePageClick}
  containerClassName={"pagination-container"}
  pageClassName={"page-item"}
  pageLinkClassName={"page-link"}
  previousClassName={"page-button"}
  nextClassName={"page-button"}
  activeClassName={"active"}
  disabledClassName={"disabled"}
/>

    </div>
  );
};

export default Dashboard;
